const mongoose = require("mongoose");

// We want this user model to contain these types of data
// Name - Password - Avatar - Date created
// Each section would have their own requirement.
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});
